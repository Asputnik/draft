/*Написать программу, на языке C#, которая из имеющегося массива строк формирует новый массив из строк,
длина которых меньше, либо равна 3 символам. Первоначальный массив можно ввести с клавиатуры,
либо задать на старте выполнения алгоритма.
При решении не рекомендуется пользоваться коллекциями, лучше обойтись исключительно массивами.*/

// Вводим первоначальный массив строк с клавиатуры
{
    Console.WriteLine("Введите строки через пробел:");
    string[] input = Console.ReadLine().Split(' ');

// Формируем новый массив из строк, длина которых меньше или равна 3 символам
    string[] result = new string[input.Length];
    int i = 0;
    for (i = 0; i < input.Length; i++)
    {
        if (input[i].Length <= 3)
        {
            result[i] = input[i];
        }
    }

// Выводим новый массив на экран
    Console.WriteLine("Строки длиной меньше или равной 3 символам:");
    for (int j = 0; j < result.Length; j++)
    {
        Console.Write(result[j] + " ");
    }
}
